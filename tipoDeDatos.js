//Tipos de datos primitivos

var int=1; //Datos numerico entero
var double=0.250; //Datos numerico con decimales
var string="Hola Mundo"; //Dato tipo string
var bool=true; //Datos booleanos
var nulo = null; //Tipo de dato nulo
var undefine = undefined //Tipo de dato indefinido


console.log(int);
console.log(double);
console.log(string);
console.log(bool);
console.log(nulo);

//Saber que tipo de datos en una variable
console.log(typeof int);
console.log(typeof double);
console.log(typeof string);
console.log(typeof bool);
console.log(typeof nulo); //Los tipos de datos nulos son de tipo Objeto.
console.log(typeof undefine);